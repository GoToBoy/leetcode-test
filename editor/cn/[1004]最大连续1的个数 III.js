/**
 * @param {number[]} nums
 * @param {number} k
 * @return {number}
 */
var longestOnes = function(nums, k) {
    var r = 0, l =0, ans = 0, zeros = [];
    while(r< nums.length){

        if(nums[r] === 0){
            zeros++;
        }
        while(zeros > k){
            if(nums[l] === 0) zeros--;
            l++
        }
        ans = Math.max(ans,r-l+1)
        r++;
    }
    return ans;
};

